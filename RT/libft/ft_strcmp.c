#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (s1[i] != '\0')
		i++;
	while (c <= i)
	{
		if (s1[c] != s2[c])
			return ((unsigned char)s1[c] - (unsigned char)s2[c]);
		c++;
	}
	return (0);
}
