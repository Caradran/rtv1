int		ft_pgcd(int a, int b)
{
	int c;
	int d;
	int r;

	if (a > b)
	{
		c = a;
		d = b;
	}
	else
	{
		c = b;
		d = a;
	}
	r = c % d;
	while (r)
	{
		c = d;
		d = r;
		r = c % d;
	}
	return (d);
}
