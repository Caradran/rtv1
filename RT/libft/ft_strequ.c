#include "libft.h"

int	ft_strequ(const char *s1, const char *s2)
{
	int		i;
	int		c;

	if (s1 == NULL || s2 == NULL)
		return (-1);
	i = 0;
	c = 0;
	while (s1[i] != '\0')
		i++;
	while (c <= i)
	{
		if (s1[c] != s2[c])
			return (0);
		c++;
	}
	return (1);
}
