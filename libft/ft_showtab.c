#include <unistd.h>

void	ft_showtab(char **tab)
{
	int i;
	int j;

	i = 0;
	while (tab[i])
	{
		j = 0;
		while (tab[i][j])
			write(1, &tab[i][j++], 1);
		i++;
		write(1, "\n", 1);
	}
}
