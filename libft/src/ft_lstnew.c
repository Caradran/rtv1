#include <stdlib.h>
#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*list;

	if ((list = (t_list *)malloc(sizeof(t_list))) == NULL)
		return (NULL);
	if (content == NULL)
	{
		list->content = NULL;
		list->content_size = 0;
	}
	else
	{
		if ((list->content = ft_memdup(content, content_size)) == NULL)
		{
			free(list);
			return (NULL);
		}
		list->content_size = content_size;
	}
	list->next = NULL;
	return (list);
}
