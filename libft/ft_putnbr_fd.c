#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char c;

	if (n >= -9 && n <= 9)
	{
		if (n < 0)
		{
			write(fd, "-", 1);
			n *= -1;
		}
		c = n + 48;
		write(fd, &c, 1);
		return ;
	}
	ft_putnbr_fd(n / 10, fd);
	if (n > 0)
		c = n % 10 + 48;
	else
		c = (-(n % 10) + 48);
	write(fd, &c, 1);
}
